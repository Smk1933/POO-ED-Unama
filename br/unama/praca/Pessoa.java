package br.unama.praca;

public class Pessoa {

    private String name;
    private String cpf; 

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCpf() {
        return this.cpf;     
    }
    
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

}
